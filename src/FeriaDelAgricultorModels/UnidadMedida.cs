namespace FeriaDelAgricultorModels
{
    /// <summary>
    /// Representa las unidades de medida utilizadas para los productos
    /// vendidos en la Feria del Agricultor.
    /// </summary>
    public enum UnidadMedida
    {
        /// <summary>
        /// Producto vendido por unidades (ejemplo: huevos, frutas individuales).
        /// </summary>
        Unidades = 1,

        /// <summary>
        /// Producto vendido por kilogramos.
        /// </summary>
        Kilogramos = 2,

        /// <summary>
        /// Producto vendido por litros.
        /// </summary>
        Litros = 3
    }
}
